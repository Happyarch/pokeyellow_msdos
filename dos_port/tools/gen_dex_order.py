#!/usr/bin/env python3
"""gen_dex_order.py — generate assets/dex_order.inc.

PokedexOrder: a flat `db` table indexed by (internal index - 1), where each byte
is that internal-index mon's NATIONAL POKéDEX NUMBER. This is exactly pret's
data/pokemon/dex_order.asm (`table_width 1`, 190 `db DEX_*`), with the `DEX_*`
constants resolved to their numeric values via constants/pokedex_constants.asm.

pokedex.asm's PokedexToIndex walks this list to convert a dex number -> internal
index; IndexToPokedex indexes it directly to convert internal index -> dex
number (see memory `gen1-internal-index-vs-dex`).

Source of truth (read-only): data/pokemon/dex_order.asm +
constants/pokedex_constants.asm. Tier-1 machine output (CLAUDE.md two-tier
rule) — DO NOT EDIT BY HAND. Consumed by src/engine/menus/pokedex.asm (S8 pkg G).
"""
import re
import sys
from pathlib import Path

DOS = Path(__file__).resolve().parent.parent          # dos_port/
ROOT = DOS.parent                                      # repo root (pret)
ORDER = ROOT / "data" / "pokemon" / "dex_order.asm"
DEXCONST = ROOT / "constants" / "pokedex_constants.asm"
OUT = DOS / "assets" / "dex_order.inc"


def parse_dex_constants():
    """DEX_* -> value via const_def / const (constants/pokedex_constants.asm)."""
    cv, defs = 0, {}
    for raw in DEXCONST.read_text(encoding="utf-8").splitlines():
        s = raw.split(";")[0].strip()
        if not s:
            continue
        m = re.match(r'const_def\s+(\d+)$', s)
        if m:
            cv = int(m.group(1)); continue
        m = re.match(r'const_def$', s)
        if m:
            cv = 0; continue
        m = re.match(r'const\s+(\w+)$', s)
        if m:
            defs[m.group(1)] = cv; cv += 1; continue
    return defs


def parse_order(defs):
    """Return the list of dex-number bytes from PokedexOrder (internal-index order)."""
    nums = []
    in_table = False
    for raw in ORDER.read_text(encoding="utf-8").splitlines():
        s = raw.split(";")[0].strip()
        if s.startswith("PokedexOrder"):
            in_table = True
            continue
        if not in_table or not s:
            continue
        if s.startswith("table_width"):
            continue
        m = re.match(r'db\s+(\w+)$', s)
        if not m:
            # end of the db block
            break
        name = m.group(1)
        if name.isdigit():
            nums.append(int(name))      # MissingNo slots are literal `db 0`
        elif name in defs:
            nums.append(defs[name])
        else:
            sys.exit(f"gen_dex_order: unresolved constant {name}")
    return nums


def emit(nums):
    lines = [
        "; assets/dex_order.inc — DO NOT EDIT BY HAND (generated by",
        "; tools/gen_dex_order.py from data/pokemon/dex_order.asm +",
        "; constants/pokedex_constants.asm).",
        ";",
        "; PokedexOrder[internal_index - 1] = national dex number. Walked by",
        "; PokedexToIndex / indexed by IndexToPokedex (src/engine/menus/pokedex.asm).",
        f"; {len(nums)} entries (internal indices 1..{len(nums)}).",
        "",
        "PokedexOrder:",
    ]
    # 16 bytes per db line, decimal (matches pret's byte semantics)
    for i in range(0, len(nums), 16):
        chunk = ", ".join(str(n) for n in nums[i:i + 16])
        lines.append(f"    db {chunk}")
    lines.append("")
    OUT.write_text("\n".join(lines), encoding="utf-8")
    print(f"gen_dex_order: wrote {OUT} ({len(nums)} entries)")


def main():
    defs = parse_dex_constants()
    nums = parse_order(defs)
    if not nums:
        sys.exit("gen_dex_order: no PokedexOrder entries parsed")
    emit(nums)


if __name__ == "__main__":
    main()
