#!/usr/bin/env python3
"""gen_menu_strings.py — generate dos_port/assets/menu_strings.inc.

Encodes the START-menu item labels through the unicode_converter submodule
(gb_text.encode) instead of hand-written charmap hex, then emits NASM `db` lines
with the '@' string terminator ($50). Consumed by
src/engine/menus/start_menu.asm via %include.

The label names match the symbols start_menu.asm references. All labels use only
single-char glyphs (incl. 'é' $BA), so they round-trip exactly through the
single-char converter (see gb_text.py).

Run from repo root or dos_port/.
"""
import os
import sys
from pathlib import Path

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
import gb_text  # noqa: E402

ROOT = Path(__file__).resolve().parents[2]
ASSETS = ROOT / "dos_port" / "assets"
TERMINATOR = 0x50

# (NASM label, Unicode label text) — matches src/engine/menus/start_menu.asm.
LABELS = [
    ("sm_str_pokedex", "POKéDEX"),
    ("sm_str_pokemon", "POKéMON"),
    ("sm_str_item",    "ITEM"),
    ("sm_str_save",    "SAVE"),
    ("sm_str_reset",   "RESET"),
    ("sm_str_option",  "OPTION"),
    ("sm_str_exit",    "EXIT"),
]


def main() -> int:
    out = [
        "; menu_strings.inc — generated by tools/gen_menu_strings.py. DO NOT EDIT BY HAND.",
        "; START-menu item labels, GB-charmap encoded via the unicode_converter",
        "; submodule (gb_text.encode) and '@'-terminated ($50).",
        "",
    ]
    for label, text in LABELS:
        b = gb_text.encode(text) + [TERMINATOR]
        hexs = ", ".join(f"0x{x:02X}" for x in b)
        out.append(f"{label}: db {hexs}   ; {text}")
    out.append("")

    ASSETS.mkdir(parents=True, exist_ok=True)
    dst = ASSETS / "menu_strings.inc"
    dst.write_text("\n".join(out))
    print(f"wrote {dst} ({len(LABELS)} labels)")
    return 0


if __name__ == "__main__":
    sys.exit(main())
