#!/usr/bin/env python3
"""gen_audio_data.py — generate the DOS port's audio data assets.

Outputs (all Tier-1 generated data, DO NOT EDIT BY HAND):
  assets/audio_rom.inc       four 16 KB GB-audio-bank images (banks $02, $08,
                             $1F, $20) at their true GB addresses, so every
                             16-bit pointer the translated engine dereferences
                             (headers, sound_call/sound_loop targets, wave
                             table, pitch table, pokeflute channel hijack,
                             Audio2_CryRet) resolves inside the blob as
                             offset = bank_slot*$4000 + (ptr - $4000).
                             Gaps where engine *code* lives in the ROM are
                             zero-filled and never dereferenced.
  assets/audio_constants.inc positional music/SFX sound IDs (id = (header -
                             $4000)/3, shared numbering across banks), the
                             boundary constants (NOISE_INSTRUMENTS_END,
                             CRY_SFX_*, BATTLE_SFX_*, MAX_SFX_ID_N), and the
                             vendored GB addresses of engine-referenced
                             tables.
  assets/cry_data.inc        CryData: 3 bytes per species index (base-cry
                             index, pitch mod, length mod), from
                             data/pokemon/cries.asm.

Fidelity is proven by tools/audio/test_audio_data.py, which byte-compares
all of the above against a pristine pret pokeyellow.gbc build.
"""

from __future__ import annotations

import re
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
from pret_audio import (  # noqa: E402
    AudioROM, BANK_BASE, BANK_SIZE, VENDORED_SYMBOLS, parse_int,
)

ROOT = Path(__file__).resolve().parents[3]
ASSETS = ROOT / "dos_port" / "assets"

HEADER = "; {name} — generated by tools/audio/gen_audio_data.py. DO NOT EDIT BY HAND.\n"


# ---------------------------------------------------------------------------
# constants/music_constants.asm: NAME -> header label (+ boundary defs)
# ---------------------------------------------------------------------------
_MUSIC_CONST_RE = re.compile(r"^music_const\s+(\w+),\s*(\w+)$")
_DEF_EQUS_RE = re.compile(r'^DEF\s+(\w+)\s+EQUS\s+"([^"]+)"$')
_DEF_EQU_RE = re.compile(r"^DEF\s+(\w+)\s+EQU\s+(\$?\w+)$")


def parse_music_constants() -> tuple[dict[str, str], dict[str, str]]:
    """Returns (name -> header label, boundary name -> expression)."""
    consts: dict[str, str] = {}
    bounds: dict[str, str] = {}
    for line in (ROOT / "constants/music_constants.asm").read_text().splitlines():
        code = line.split(";", 1)[0].strip()
        if not code:
            continue
        m = _MUSIC_CONST_RE.match(code)
        if m:
            consts[m.group(1)] = m.group(2)
            continue
        m = _DEF_EQUS_RE.match(code) or _DEF_EQU_RE.match(code)
        if m and m.group(1) != "NOISE_INSTRUMENTS_START":
            bounds[m.group(1)] = m.group(2)
    return consts, bounds


def sound_id(rom: AudioROM, label: str) -> int:
    addr = rom.symtab[label]
    off = addr - BANK_BASE
    if off % 3:
        raise ValueError(f"{label} at ${addr:04x} is not 3-byte aligned")
    return off // 3


def eval_boundary(expr: str, ids: dict[str, int]) -> int:
    """Evaluate 'NAME', 'NAME + n', or '$ff' boundary expressions."""
    expr = expr.strip()
    m = re.match(r"^(\w+)\s*\+\s*(\d+)$", expr)
    if m:
        return ids[m.group(1)] + int(m.group(2))
    if expr in ids:
        return ids[expr]
    return parse_int(expr)


# ---------------------------------------------------------------------------
# data/pokemon/cries.asm
# ---------------------------------------------------------------------------
_MON_CRY_RE = re.compile(r"^mon_cry\s+(\w+),\s*(\$?\w+),\s*(\$?\w+)$")


def parse_cry_data(ids: dict[str, int]) -> list[tuple[int, int, int, str]]:
    """(base-cry index, pitch, length, comment) per species index."""
    out = []
    base = ids["SFX_CRY_00"]
    for line in (ROOT / "data/pokemon/cries.asm").read_text().splitlines():
        code, _, comment = line.partition(";")
        code = code.strip()
        m = _MON_CRY_RE.match(code)
        if not m:
            continue
        delta = ids[m.group(1)] - base
        if delta % 3:
            raise ValueError(f"cry {m.group(1)}: id delta {delta} not /3")
        out.append((delta // 3, parse_int(m.group(2)), parse_int(m.group(3)),
                    comment.strip()))
    return out


# ---------------------------------------------------------------------------
# emitters
# ---------------------------------------------------------------------------
def emit_audio_rom(rom: AudioROM) -> str:
    lines = [HEADER.format(name="audio_rom.inc")]
    lines.append("; Four GB audio banks at true addresses ($4000-$7FFF each);")
    lines.append("; zero-filled where ROM holds engine code (never dereferenced).")
    lines.append("")
    lines.append("AudioRom:")
    for slot, bank in enumerate(rom.banks()):
        img, _ = rom.render_bank(bank)
        lines.append(f"AudioRom_Bank{bank:02X}:  ; slot {slot}, GB bank ${bank:02x}")
        for i in range(0, BANK_SIZE, 16):
            row = ", ".join(f"0x{b:02X}" for b in img[i:i + 16])
            lines.append(f"    db {row}")
    lines.append("AudioRomEnd:")
    lines.append("")
    return "\n".join(lines)


def emit_constants(rom: AudioROM, ids: dict[str, int],
                   bounds: dict[str, str]) -> str:
    lines = [HEADER.format(name="audio_constants.inc")]
    lines.append("; Positional sound IDs: id = (header_addr - $4000) / 3;")
    lines.append("; music, SFX, cries and noise instruments share the space.")
    lines.append("")
    lines.append("%ifndef AUDIO_CONSTANTS_INC")
    lines.append("%define AUDIO_CONSTANTS_INC")
    lines.append("")
    for name, id_val in ids.items():
        lines.append(f"{name:<28} equ 0x{id_val:02X}")
    lines.append("")
    lines.append("; boundary constants (constants/music_constants.asm)")
    for name, expr in bounds.items():
        lines.append(f"{name:<28} equ 0x{eval_boundary(expr, ids):02X}")
    lines.append("")
    lines.append("; GB bank numbers of the four audio banks, slot order")
    for slot, bank in enumerate(rom.banks()):
        lines.append(f"AUDIO_BANK_{slot + 1:<17} equ 0x{bank:02X}")
    lines.append("")
    lines.append("; GB addresses of in-blob data referenced from engine/game code")
    for label in ("Audio1_Pitches", "Audio1_WavePointers",
                  "Audio1_CryRet", "Audio2_CryRet",
                  "Audio3_CryRet", "Audio4_CryRet",
                  "SFX_Pokeflute_Ch5", "SFX_Pokeflute_Ch6", "SFX_Pokeflute_Ch7",
                  "Music_MeetRival_Ch1_AlternateStart",
                  "Music_MeetRival_Ch2_AlternateStart",
                  "Music_MeetRival_Ch3_AlternateStart",
                  "Music_MeetRival_Ch1_AlternateTempo",
                  "Music_MeetRival_Ch1_AlternateStartAndTempo",
                  "Music_Cities1_Ch1_AlternateTempo"):
        addr = rom.symtab[label]
        name = "GB_" + re.sub(r"[^A-Za-z0-9]", "_", label).upper()
        lines.append(f"{name:<28} equ 0x{addr:04X}  ; bank ${rom.sym_bank[label]:02x}")
    lines.append("")
    lines.append("%endif ; AUDIO_CONSTANTS_INC")
    lines.append("")
    return "\n".join(lines)


def emit_cry_data(cries) -> str:
    lines = [HEADER.format(name="cry_data.inc")]
    lines.append("; CryData: db base-cry index, pitch mod, length mod (3 bytes")
    lines.append("; per species index, data/pokemon/cries.asm order).")
    lines.append("")
    lines.append("CryData:")
    for base, pitch, length, comment in cries:
        row = f"    db 0x{base:02X}, 0x{pitch:02X}, 0x{length:02X}"
        lines.append(f"{row}    ; {comment}" if comment else row)
    lines.append("CryDataEnd:")
    lines.append("")
    return "\n".join(lines)


def main():
    rom = AudioROM(ROOT)
    consts, bounds = parse_music_constants()
    # SFX_UNKNOWN_802CC_4 -> SFX_802cc_4 dangles even in pret (music_const
    # is a lazy EQUS and nothing ever expands it; the header label is
    # actually SFX_Unknown_802cc). Skip unresolvable names.
    ids = {name: sound_id(rom, label)
           for name, label in consts.items() if label in rom.symtab}
    skipped = [n for n, l in consts.items() if l not in rom.symtab]
    if skipped != ["SFX_UNKNOWN_802CC_4"]:
        raise KeyError(f"unexpected unresolvable music consts: {skipped}")

    (ASSETS / "audio_rom.inc").write_text(emit_audio_rom(rom))
    (ASSETS / "audio_constants.inc").write_text(emit_constants(rom, ids, bounds))
    (ASSETS / "cry_data.inc").write_text(emit_cry_data(parse_cry_data(ids)))
    print(f"audio_rom.inc: {len(rom.banks())} banks x {BANK_SIZE} bytes; "
          f"{len(ids)} sound IDs; cry_data written")


if __name__ == "__main__":
    main()
