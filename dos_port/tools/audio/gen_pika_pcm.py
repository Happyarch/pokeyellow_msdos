#!/usr/bin/env python3
"""gen_pika_pcm.py — Pikachu voice clips → 8-bit PCM blob for the DOS players.

The pret WAVs (audio/pikachu_cries/pikachu_cry_N.wav) are the raw 1-bit GB
streams rendered as 0/255 bytes at 22050 Hz (one byte per source bit — the
.pcm INCBINs are the same stream packed 8 bits/byte). A bare 8-bit DAC
rendering of that square stream sounds exactly as harsh as the GB's wave-DAC
hack did, plus quantization hiss, so this generator does what the Game Boy's
analog output stage did for free: low-pass filter. Then it decimates to
PIKA_PCM_RATE, normalizes all clips by one shared gain (relative loudness
preserved), and rounds to 8-bit unsigned centered on 0x80.

Outputs (both Tier-1 generated, DO NOT EDIT BY HAND):
  assets/pika_pcm.bin — all 42 clips concatenated
  assets/pika_pcm.inc — PikachuCriesPointerTable (pret name; port format:
                        dd ptr, dd sample count) + per-clip PikachuCryN
                        labels over one incbin

Consumers: src/audio/sb_pcm.asm (DSP direct mode), src/audio/spk_pcm.asm
(PIT ch2 PWM), dispatched by PlayPikachuSoundClip
(src/engine/pikachu/pikachu_pcm.asm).
"""

from pathlib import Path
import struct
import wave

import numpy as np

ROOT = Path(__file__).resolve().parents[3]
WAV_DIR = ROOT / "audio" / "pikachu_cries"
ASSETS = ROOT / "dos_port" / "assets"

NUM_CLIPS = 42
SRC_RATE = 22050
DECIMATE = 2
OUT_RATE = SRC_RATE // DECIMATE        # 11025 Hz — the players pace to this

FIR_TAPS = 127                         # Blackman-windowed sinc
CUTOFF_HZ = 4500                       # voice content; kills the 1-bit hash
FADE_SAMPLES = 32                      # ~3 ms edge ramps (kill DC-step clicks)
PEAK_TARGET = 0.95


def lowpass_kernel() -> np.ndarray:
    fc = CUTOFF_HZ / SRC_RATE
    n = np.arange(FIR_TAPS) - (FIR_TAPS - 1) / 2
    h = np.sinc(2 * fc * n) * np.blackman(FIR_TAPS)
    return h / h.sum()


def load_clip(n: int) -> np.ndarray:
    with wave.open(str(WAV_DIR / f"pikachu_cry_{n}.wav")) as w:
        assert (w.getframerate(), w.getsampwidth(), w.getnchannels()) == \
               (SRC_RATE, 1, 1), f"clip {n}: unexpected WAV format"
        raw = np.frombuffer(w.readframes(w.getnframes()), dtype=np.uint8)
    return raw.astype(np.float64) / 127.5 - 1.0


def main():
    kern = lowpass_kernel()
    clips = []
    for n in range(1, NUM_CLIPS + 1):
        x = np.convolve(load_clip(n), kern, mode="same")[::DECIMATE]
        x -= x.mean()                            # per-clip DC removal
        ramp = np.linspace(0.0, 1.0, FADE_SAMPLES)
        x[:FADE_SAMPLES] *= ramp
        x[-FADE_SAMPLES:] *= ramp[::-1]
        clips.append(x)

    gain = PEAK_TARGET / max(np.abs(c).max() for c in clips)
    blob = bytearray()
    offsets = []
    for x in clips:
        offsets.append((len(blob), len(x)))
        u8 = np.clip(np.rint(x * gain * 127.5 + 128.0), 0, 255)
        blob += u8.astype(np.uint8).tobytes()

    (ASSETS / "pika_pcm.bin").write_bytes(blob)

    lines = [
        "; pika_pcm.inc — generated by tools/audio/gen_pika_pcm.py. "
        "DO NOT EDIT BY HAND.",
        ";",
        f"; {NUM_CLIPS} Pikachu voice clips, 8-bit unsigned mono @ "
        f"{OUT_RATE} Hz (low-pass",
        f"; filtered from the 1-bit 22050 Hz pret streams, cutoff "
        f"{CUTOFF_HZ} Hz).",
        "; Table keeps the pret name; port entry format: dd ptr, dd samples.",
        "",
        f"PIKA_PCM_RATE   equ {OUT_RATE}",
        f"NUM_PIKA_CRIES  equ {NUM_CLIPS}",
        "",
        "PikachuCriesPointerTable:",
    ]
    for i, (off, cnt) in enumerate(offsets, 1):
        lines.append(f"    dd PikachuCry{i}, {cnt}")
    lines += ["", 'PikaPcmData: incbin "assets/pika_pcm.bin"', ""]
    for i, (off, cnt) in enumerate(offsets, 1):
        lines.append(f"PikachuCry{i:<2} equ PikaPcmData + {off}")
    lines.append("")
    (ASSETS / "pika_pcm.inc").write_text("\n".join(lines))

    total = len(blob)
    print(f"pika_pcm: {NUM_CLIPS} clips, {total} bytes "
          f"({total / OUT_RATE:.1f} s) @ {OUT_RATE} Hz -> "
          f"{ASSETS / 'pika_pcm.bin'}")


if __name__ == "__main__":
    main()
