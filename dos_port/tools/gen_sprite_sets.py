#!/usr/bin/env python3
"""gen_sprite_sets.py — generate assets/sprite_sets.inc (Tier-1 data).

Faithful port of pret data/maps/sprite_sets.asm: the fixed outside-map sprite
sets and their split-map variants, consumed by the OW-A.2 map_sprites.asm
sprite-set machinery (_InitMapSprites / InitOutsideMapSprites / GetSplitMapSpriteSetID
/ LoadSpriteSetFromMapHeader).

Emits three tables:
  MapSpriteSets       — 1 byte/outdoor map (FIRST_INDOOR_MAP entries): sprite-set id
  SplitMapSpriteSets  — 4 bytes/row (NUM_SPLIT_SETS rows): dir, divide, west/north, east/south
  SpriteSets          — SPRITE_SET_LENGTH (11) sprite ids per set, NUM_SPRITE_SETS (10) sets

Set-ids (SPRITESET_*/SPLITSET_*) and split dirs (EAST_WEST/NORTH_SOUTH) are emitted
symbolically — they are stable constants in include/gb_memmap.inc.  Sprite ids in
SpriteSets are numeric with a SPRITE_* comment (the full SPRITE_* enum is not defined
port-side), matching the port's generated-table convention (npc_sprite_data_table.inc).

DO NOT hand-edit the output; fix this generator and re-run `make assets`.
"""
import re
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]          # repo root (pret source)
PORT = Path(__file__).resolve().parents[1]           # dos_port/
SRC = ROOT / "data" / "maps" / "sprite_sets.asm"
SPRITE_CONSTS = ROOT / "constants" / "sprite_constants.asm"
OUT = PORT / "assets" / "sprite_sets.inc"


def parse_sprite_constants() -> dict:
    """constants/sprite_constants.asm → {SPRITE_FOO: id} (const_def, base 0)."""
    result = {}
    sid = 0
    for line in SPRITE_CONSTS.read_text().splitlines():
        m = re.match(r"\s*const\s+(SPRITE_\w+)", line)
        if m:
            result[m.group(1)] = sid
            sid += 1
    return result


def db_token(line: str) -> str:
    """First `db` operand on a line, stripped of comment/whitespace."""
    body = line.split("db", 1)[1]
    body = body.split(";", 1)[0]
    return body.strip()


def comment(line: str) -> str:
    return line.split(";", 1)[1].strip() if ";" in line else ""


def main() -> None:
    text = SRC.read_text().splitlines()
    names = parse_sprite_constants()

    # --- slice the three table bodies by their labels -----------------------
    def section(start_label: str, end_marker: str) -> list:
        out, on = [], False
        for line in text:
            if line.strip().startswith(start_label):
                on = True
                continue
            if on and end_marker in line:
                break
            if on:
                out.append(line)
        return out

    map_body = section("MapSpriteSets:", "SplitMapSpriteSets:")
    split_body = section("SplitMapSpriteSets:", "SpriteSets:")
    set_body = section("SpriteSets:", "assert_table_length NUM_SPRITE_SETS")

    L = []
    L.append("; sprite_sets.inc — generated by tools/gen_sprite_sets.py. DO NOT EDIT BY HAND.")
    L.append("; Faithful port of pret data/maps/sprite_sets.asm (OW-A.2 sprite-set machinery).")
    L.append("; Consumed by src/engine/overworld/map_sprites.asm; needs the SPRITESET_*/SPLITSET_*")
    L.append("; + EAST_WEST/NORTH_SOUTH constants from include/gb_memmap.inc (included first).")
    L.append("")

    # MapSpriteSets — 1 byte/map, symbolic set id, keep pret's map-name comment.
    L.append("MapSpriteSets:")
    n_map = 0
    for line in map_body:
        if "db" not in line:
            continue
        tok, c = db_token(line), comment(line)
        L.append(f"    db {tok}" + (f" ; {c}" if c else ""))
        n_map += 1
    L.append("")

    # SplitMapSpriteSets — 4 bytes/row, symbolic, keep pret's SPLITSET_* comment.
    L.append("SplitMapSpriteSets:")
    n_split = 0
    for line in split_body:
        if "db" not in line:
            continue
        tok, c = db_token(line), comment(line)
        L.append(f"    db {tok}" + (f" ; {c}" if c else ""))
        n_split += 1
    L.append("")

    # SpriteSets — SPRITE_SET_LENGTH sprite ids per set; numeric id + SPRITE_* comment.
    L.append("SpriteSets:")
    n_sets, n_entries = 0, 0
    for line in set_body:
        s = line.strip()
        if s.startswith("; SPRITESET_"):
            L.append(f"    {s}")            # set header comment, from pret
            n_sets += 1
            continue
        if "db" not in line:
            continue
        name = db_token(line)
        if name not in names:
            print(f"ERROR: unknown sprite constant {name!r} in SpriteSets", file=sys.stderr)
            sys.exit(1)
        L.append(f"    db 0x{names[name]:02X} ; {name}")
        n_entries += 1
    L.append("")

    OUT.write_text("\n".join(L) + "\n")
    print(f"wrote {OUT.relative_to(PORT)}: "
          f"MapSpriteSets={n_map} SplitMapSpriteSets={n_split} "
          f"SpriteSets={n_sets} sets / {n_entries} entries")

    # sanity: pret invariants
    assert n_map == 0x25, f"MapSpriteSets={n_map}, expected FIRST_INDOOR_MAP=0x25"
    assert n_split == 12, f"SplitMapSpriteSets={n_split}, expected NUM_SPLIT_SETS=12"
    assert n_sets == 10 and n_entries == 110, \
        f"SpriteSets={n_sets} sets/{n_entries} entries, expected 10/110"


if __name__ == "__main__":
    main()
